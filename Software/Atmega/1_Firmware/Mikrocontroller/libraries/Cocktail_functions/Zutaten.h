// Funktion zur Alloziierung des Speicherplatzes für zutatMaschine_t
zutatMaschine_t *create_neue_zutat_Maschine(char * name, char status, uint8_t alkohol, uint8_t kohlensaeure, uint8_t k, uint32_t positon_motor);



